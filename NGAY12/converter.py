# Tạo module converter.py chứa các hàm:
# ●	km_to_miles(km)
# ●	miles_to_km(miles)
# ●	celsius_to_fahrenheit(c)
# ●	fahrenheit_to_celsius(f)
def km_to_miles(km):
    return km*0.621371
def miles_to_km(miles):
    return miles/0.621371
def celsius_to_fahrenheit(c):
    return c*1.8+32
def fahrenheit_to_celsius(f):
    return (f-32)/1.8