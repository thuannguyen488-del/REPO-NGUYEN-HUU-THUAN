# Nhập tuổi của học sinh và kiểm tra xem học sinh đấy có đủ tuổi vào học
#  lớp 1 hay không và in ra kết quả ra màn hình.
# ( > 6 tuổi thì được vào học lớp 1) 
tuoi=int(input("nhập vào tuổi học sinh:"))
if tuoi>=6:
   print("học sinh được học lớp 1")
else:
   print("học sinh chưa đủ tuổi học lớp 1")